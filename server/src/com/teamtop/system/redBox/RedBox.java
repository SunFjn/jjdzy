package com.teamtop.system.redBox;

public class RedBox{
	
	private long hid;
	
	private long goldYuanBao;
	
	private int sendNum;
	

	public RedBox() {
		super();
	}

	public long getHid() {
		return hid;
	}

	public void setHid(long hid) {
		this.hid = hid;
	}

	public long getGoldYuanBao() {
		return goldYuanBao;
	}

	public void setGoldYuanBao(long goldYuanBao) {
		this.goldYuanBao = goldYuanBao;
	}

	public int getSendNum() {
		return sendNum;
	}

	public void setSendNum(int sendNum) {
		this.sendNum = sendNum;
	}
	
	

}
