package com.teamtop.system.bag;

public class BagBugItem {
	
	
	private long rid;
	
	private int sysId;
	
	private int num;
	
	private int time;
	

	public BagBugItem() {
		super();
	}

	public long getRid() {
		return rid;
	}

	public void setRid(long rid) {
		this.rid = rid;
	}

	public int getSysId() {
		return sysId;
	}

	public void setSysId(int sysId) {
		this.sysId = sysId;
	}

	public int getNum() {
		return num;
	}

	public void setNum(int num) {
		this.num = num;
	}

	public int getTime() {
		return time;
	}

	public void setTime(int time) {
		this.time = time;
	}
	

	
}
